<h3 id="section2">Редактирование</h3>

<p>Для редактирования  содержимого строки нажмите иконку <b>«Изменить»</b>, которая расположена в конце каждой строки.</p>
<img class="im mgb" src="./img/hard/sm7.jpg" />
<p>После внесения изменений нажмите <b>«Сохранить»</b> — произойдет возврат к таблице</p>
<img class="im mgb" src="./img/hard/sm8.jpg" />

<h3 id="section3">Удаление</h3>

<p>
  Удаление одной строки происходит нажатием на иконку <b>«Удалить»</b>,
  которая располагается в конце каждой строки.
</p>
<img class="im mgb" src="./img/hard/sm9.jpg" />
<p>
  Удалите несколько строк сразу: отметьте флажки слева и нажмите
  <b>«Удалить»</b> на панели действий.
</p>
